
public class Model {
	
	    
	    public double  x,y;  
	    
	   public Model(){
	   x=0;
	   y=0;
	   }
	    
	    public double multiply(double x,double y){
	    
	        double result=x*y;
	        return result;
	    
	    }
	    
	    public double division(double x,double y){
	    
	        double result=x/y;
	        return result;
	    
	    }
	     
	     public  double subtraction(double x,double y){
	    
	        double result=x-y;
	        return result;
	    
	    }
	      
	       public  double addition(double x,double y){
	    
	        double result=x+y;
	        return result;
	    
	    }
	       
	        public double modul(double x,double y){
	    
	        double result=x%y;
	        return result;
	    
	    }        
	 
	       
	    
	    
	    
	}


